# this function returns true if the entered views matches the set views 9.3.
def views_in_millions(views):
    return bool(views == 9.3)  # same as return x==9.3


# returns the variable content passed to parameter y
def language(y): return y


# returns the variable content passed to parameter m

def message(m): return m


# # returns the variable content passed to parameter country
def country_origin(country): return country


